SELECT q.id, qt.id
FROM link_source ls
JOIN quotes q
  ON q.content = ls.quote_content
JOIN quote_topics qt
  ON qt.code = ls.topic_code
ON CONFLICT (quote_id, quote_topic_id) DO NOTHING;

